package QMS;

public class patient {

	String last_name;
	String first_name;
	int age;
	float weight;
	float height;
	String gender;
	
	public patient(String l, String f, int a, float w, float h, String g) {
		
		last_name = l;
		first_name = f;
		age = a;
		weight = w;
		height = h;
		gender = g;
		
	}
	
}
